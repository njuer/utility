public class ShiShiCai {

	private Integer period;
	private Integer ge;
	private Integer shi;
	private Integer bai;
	private Integer qian;
	private Integer wan;
	private String category;
	
	public Integer getPeriod() {
		return period;
	}
	public void setPeriod(Integer period) {
		this.period = period;
	}
	public Integer getGe() {
		return ge;
	}
	public void setGe(Integer ge) {
		this.ge = ge;
	}
	public Integer getShi() {
		return shi;
	}
	public void setShi(Integer shi) {
		this.shi = shi;
	}
	public Integer getBai() {
		return bai;
	}
	public void setBai(Integer bai) {
		this.bai = bai;
	}
	public Integer getQian() {
		return qian;
	}
	public void setQian(Integer qian) {
		this.qian = qian;
	}
	public Integer getWan() {
		return wan;
	}
	public void setWan(Integer wan) {
		this.wan = wan;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	

}